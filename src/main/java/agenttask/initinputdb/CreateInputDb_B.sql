        occupancy_d1   DOUBLE                  NOT NULL,
        condition_d2   DOUBLE                  NOT NULL,
        occupancy_d2   DOUBLE                  NOT NULL,
        condition_d3   DOUBLE                  NOT NULL,
        occupancy_d3   DOUBLE                  NOT NULL);